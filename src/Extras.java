
public class Extras {

    private String extra;
    private int precio;

    public Extras() {
        this.extra = "";
        this.precio = 0;
    }

    public String getExtra() {
        return extra;
    }

    public void setExtra(String extra) {
        this.extra = extra;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }

}
